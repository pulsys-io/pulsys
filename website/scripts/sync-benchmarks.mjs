#!/usr/bin/env node
/**
 * sync-benchmarks.mjs — copy docs/results into public/ and emit headline.ts
 * from docs/results/ec2/headline.json (single source of truth for homepage stats).
 */
import { cpSync, mkdirSync, existsSync, readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const websiteRoot = join(__dirname, '..');
const repoRoot = join(websiteRoot, '..');
const resultsSrc = join(repoRoot, 'docs', 'results');
const resultsDest = join(websiteRoot, 'public', 'results');
const headlineJson = join(resultsSrc, 'ec2', 'headline.json');
const generatedDir = join(websiteRoot, 'src', 'generated');
const headlineTs = join(generatedDir, 'headline.ts');

if (!existsSync(resultsSrc)) {
  console.error('sync-benchmarks: missing', resultsSrc);
  process.exit(1);
}
if (!existsSync(headlineJson)) {
  console.error('sync-benchmarks: missing', headlineJson);
  console.error('  Run the EC2 benchmark harness or commit docs/results/ec2/headline.json');
  process.exit(1);
}

mkdirSync(join(websiteRoot, 'public'), { recursive: true });
cpSync(resultsSrc, resultsDest, { recursive: true });

const h = JSON.parse(readFileSync(headlineJson, 'utf8'));
const esc = (s) => String(s).replace(/\\/g, '\\\\').replace(/'/g, "\\'");

mkdirSync(generatedDir, { recursive: true });
writeFileSync(
  headlineTs,
  `/** Generated by scripts/sync-benchmarks.mjs — do not edit. */
export const headline = {
  instanceType: '${esc(h.instance_type ?? 'unknown')}',
  vcpu: ${Number(h.vcpu) || 0},
  kernel: '${esc(h.kernel ?? '')}',
  variant: '${esc(h.variant ?? '')}',
  peakRps: ${Number(h.peak_rps) || 0},
  peakRpsMillions: ${Number(h.peak_rps_millions) || 0},
  peakRpsPayload: '${esc(h.peak_rps_payload ?? '')}',
  peakGbps: ${Number(h.peak_gbps) || 0},
  peakGbPerS: ${Number(h.peak_gb_per_s) || 0},
  peakGbpsPayload: '${esc(h.peak_gbps_payload ?? '')}',
  durationPerCell: '${esc(h.duration_per_cell ?? '')}',
  gitCommit: '${esc(h.git_commit ?? '')}',
  generatedUtc: '${esc(h.generated_utc ?? '')}',
} as const;
`,
);

console.log('sync-benchmarks: copied docs/results → public/results');
console.log('sync-benchmarks: wrote src/generated/headline.ts');
